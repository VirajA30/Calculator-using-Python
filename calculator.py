num1 = int(input('enter a num 1 '))
num2 = int(input('enter a num 2 '))
num = input('enter a operator? (-,+,/,*)')
if (num=='-'):
   print(num1 - num2)
elif (num=='+'):
     print(num1 + num2)
elif (num=='/'):
     print(num1 / num2)
elif (num=='*'):
     print(num1 * num2)
else:
      print('give valid operator....!!!!')